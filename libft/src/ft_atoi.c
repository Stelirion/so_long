#include "../headers/libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		k;
	long	res;

	i = 0;
	k = 1;
	res = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			k = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (res != (res * 10 + (k * (str[i] - '0'))) / 10)
			return ((k + 1) / 2 / -1);
		res = res * 10 + ((str[i] - '0') * k);
		i++;
	}
	return (res);
}
